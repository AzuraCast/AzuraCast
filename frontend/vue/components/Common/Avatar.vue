<template>
    <a :href="serviceUrl" class="avatar" target="_blank" v-b-tooltip.hover.right :title="langAvatar"
       v-if="'' !== serviceUrl">
        <img :src="url" :style="{ width: this.width+'px', height: 'auto' }" alt="">
    </a>
</template>

<script setup>
import {useTranslate} from "~/vendor/gettext";
import {computed} from "vue";

const props = defineProps({
    url: String,
    service: String,
    serviceUrl: String,
    width: {
        type: Number,
        default: 64
    }
});

const {$gettext} = useTranslate();

const langAvatar = computed(() => {
    return $gettext(
        'Avatars are retrieved based on your e-mail address from the %{service} service. Click to manage your %{service} settings.',
        {
            service: props.service
        }
    );
});
</script>
